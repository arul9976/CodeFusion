package com.ide.codefusion.model;

public class UpdateUser {
    private String username;
    private String email;
    private String password;
    private String updated_username;
    private String updated_email;
    private String updated_password;

    public void setUsername(String username) {
        this.username = username;
    }
    public String getUsername() {
        return username;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getEmail() {
        return email;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getPassword() {
        return password;
    }

    public void setUpdated_username(String updated_username) {
        this.updated_username = updated_username;
    }

    public String getUpdated_username() {
        return updated_username;
    }

    public void setUpdated_email(String updated_email) {
        this.updated_email = updated_email;
    }

    public String getUpdated_email() {
        return updated_email;
    }

    public void setUpdated_password(String updated_password) {
        this.updated_password = updated_password;
    }

    public String getUpdated_password() {
        return updated_password;
    }



}
